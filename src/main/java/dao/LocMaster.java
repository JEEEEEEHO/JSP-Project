package dao;

public class LocMaster {
	
	private int locno;
	private String loc_name;
	private int upper_locno;
	
	public int getLocno() {
		return locno;
	}
	public void setLocno(int locno) {
		this.locno = locno;
	}
	public String getLoc_name() {
		return loc_name;
	}
	public void setLoc_name(String loc_name) {
		this.loc_name = loc_name;
	}
	public int getUpper_locno() {
		return upper_locno;
	}
	public void setUpper_locno(int upper_locno) {
		this.upper_locno = upper_locno;
	}
	
	
	
	
}
